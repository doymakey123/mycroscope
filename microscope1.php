<div class="card-body" >

    <h1 style="text-align: center;"> How to use light Microscope </h1>

    <div class="container-fluid">

        <br><br>
        
        <p style="text-align: justify;"> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Light microscopes are used by scientists and science lovers alike to magnify small specimens like bacteria. 
            They are less powerful than alternatives like electron microscopes but also much cheaper and more practical for casual use. 
            By focusing light on their lenses, they allow you to examine the smallest cellular constructs that make up the specimens. 
            But first, you need to set up a slide and adjust your microscope's light and focus!
        </p> 
        

        <!-- first step -->
        <div class="card">
            <div class="card-header">
                <h2> Part 1 - Setting up your slide</h2>
            </div>
                <br><br>
          
                <!-- starting image 1 -->
                <img src="images/1.png" class="rounded mx-auto d-block" alt="..." style="width: 300px; height: 300px;">

                <br><br>

                <p style="text-align: justify; margin-left: 10px; margin-right: 10px;"> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 
                    <span style="font-size:35px; line-height:70px;">1</span>
                    Connect your light microscope to an outlet. If your light microscope uses an illuminator, it requires power. 
                    Place your microscope on a flat surface and connect its power cord into an outlet. 
                    Now, flip on the light switch, which is typically located on the bottom of the microscope. 
                    After flipping the switch, the light should come out of the illuminator, which is the light source.
                </p>
                <p style="text-align: justify;">
                    <ul>  
                        <li style="text-align: justify; margin-left: 10px; margin-right: 10px;">
                              If your microscope uses a mirror instead of an illuminator to focus natural light onto your slide, skip this step.
                        </li>
                    </ul>
                </p>
                <!-- ending image 1 -->

                <br><br>
          
                <!-- starting image 2 -->
                <img src="images/2.png" class="rounded mx-auto d-block" alt="..." style="width: 300px; height: 300px;">

                <br><br>

                <p style="text-align: justify; margin-left: 10px; margin-right: 10px;"> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
                    <span style="font-size:35px; line-height:70px;">2</span>
                    Rotate the revolving nosepiece to the lowest power objective lens. 
                    Often, this is "3.5x" or "4x," although other microscopes might have lower or higher options. Regardless, the lowest power lens is the shortest in length. 
                    Once you hear the lens click into place, stop rotating the nosepiece.
                </p>
                <p style="text-align: justify;">
                    <ul>  
                        <li style="text-align: justify; margin-left: 10px; margin-right: 10px;">
                            Be gentle as you rotate the nosepiece to avoid breaking it or wearing it down.
                        </li>
                    </ul>
                </p>
                <!-- ending image 2 -->

                <br><br>
          
                <!-- starting image 3 -->
                <img src="images/3.png" class="rounded mx-auto d-block" alt="..." style="width: 300px; height: 300px;">

                <br><br>

                <p style="text-align: justify; margin-left: 10px; margin-right: 10px;"> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
                    <span style="font-size:35px; line-height:70px;">3</span>
                    Place a glass cover or coverslip over your specimen. 
                    If you haven't done so already, cover your specimen slide with a glass cover or coverslip. 
                    This will protect the specimen and the objective lens, which is the vertical lens that hovers over the slide.
                </p>
                <p style="text-align: justify;">
                    <ul>  
                        <li style="text-align: justify; margin-left: 10px; margin-right: 10px;">
                            If your microscope doesn't come with glass covers or coverslips, purchase them from online suppliers.
                        </li>
                    </ul>
                </p>
                <!-- ending image 3 -->

                <br><br>
          
                <!-- starting image 4 -->
                <img src="images/4.png" class="rounded mx-auto d-block" alt="..." style="width: 300px; height: 300px;">

                <br><br>

                <p style="text-align: justify; margin-left: 10px; margin-right: 10px;"> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
                    <span style="font-size:35px; line-height:70px;">4</span>
                    Mount your specimen onto the stage using its metal clips. 
                    Underneath the lens is a square, flat surface with 2 metal clips that run parallel. 
                    This is called the stage and it is responsible for holding the specimen. 
                    Press down on the back end of each stage clip to raise them and slip the slide underneath the clips. 
                    Center the slide so that each clip rests on its left and right end and the sample is directly in the middle.
                </p>
                <p style="text-align: justify;">
                    <ul>  
                        <li style="text-align: justify; margin-left: 10px; margin-right: 10px;">
                            Manually adjust the metal clips until your slide is even.
                        </li>
                        <li style="text-align: justify; margin-left: 10px; margin-right: 10px;">
                            If your microscope has a mechanical stage, move the curved metal slide holder to the side. 
                            Now, insert your specimen flush against the straight, stationary slide holder and release the curved piece so it moves back into place.
                        </li>
                    </ul>
                </p>
                <!-- ending image 4 -->

                <br><br>
          
                <!-- starting image 5 -->
                <img src="images/5.png" class="rounded mx-auto d-block" alt="..." style="width: 300px; height: 300px;">

                <br><br>

                <p style="text-align: justify; margin-left: 10px; margin-right: 10px;"> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
                    <span style="font-size:35px; line-height:70px;">5</span>
                    Rotate the focus knob/coarse adjustment knob until the objective lens hovers over the slide. 
                    The focus knob is typically the large knob located to the right of the microscope. 
                    Turning the focus knob either moves the objective lens or the stage. 
                    Adjust the objective lens until it is directly over the slide, with enough space in between to fit a piece of paper.
                </p>
                <p style="text-align: justify;">
                    <ul>  
                        <li style="text-align: justify; margin-left: 10px; margin-right: 10px;">
                            Never let the focus knob touch the coverslip.
                        </li>
                    </ul>
                </p>
                <!-- ending image 5 -->
            
        </div>

        <br><br>

        <!-- Second step -->
        <div class="card">
            <div class="card-header">
                <h2> Part 2 - Adjusting Light and Focus</h2>
            </div>
                <br><br>
          
                <!-- starting image 6 -->
                <img src="images/6.png" class="rounded mx-auto d-block" alt="..." style="width: 300px; height: 300px;">

                <br><br>

                <p style="text-align: justify; margin-left: 10px; margin-right: 10px;"> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 
                    <span style="font-size:35px; line-height:70px;">1</span>
                    Move the slide until it's focused in the center. 
                    Gently move the slide around with one of your hands so that the image is in the center of your vision. 
                    Once the light provides the clearest image, stop adjusting.
                </p>
                <p style="text-align: justify;">
                    <ul>  
                        <li style="text-align: justify; margin-left: 10px; margin-right: 10px;">
                            If you're using a low-power objective lens, you might need to decrease the light intensity or turn the condenser off.
                        </li>
                    </ul>
                </p>
                <!-- ending image 6 -->

                <br><br>
          
                <!-- starting image 7 -->
                <img src="images/7.png" class="rounded mx-auto d-block" alt="..." style="width: 300px; height: 300px;">

                <br><br>

                <p style="text-align: justify; margin-left: 10px; margin-right: 10px;"> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
                    <span style="font-size:35px; line-height:70px;">2</span>
                    Adjust the mirror, illuminator, and/or diaphragm for maximum light exposure. 
                    If your microscope has a mirror, adjust its position under the stage until it reflects the maximum amount of light onto your slide. 
                    For microscopes with illuminators, rotate the rim around the condenser that is underneath the stage until it focuses the maximum amount of light. 
                    Similarly, the diaphragm is the rotating disc located under the stage that has varying holes for different light intensities—rotate it until you achieve the maximum.
                </p>
                <p style="text-align: justify;">
                    <ul>  
                        <li style="text-align: justify; margin-left: 10px; margin-right: 10px;">
                            Whether your microscope has a mirror or illuminator, be sure that the light is focused directly onto the middle of the sample or condenser, respectively.
                        </li>
                    </ul>
                </p>
                <!-- ending image 7 -->

                <br><br>
          
                <!-- starting image 8 -->
                <img src="images/8.png" class="rounded mx-auto d-block" alt="..." style="width: 300px; height: 300px;">

                <br><br>

                <p style="text-align: justify; margin-left: 10px; margin-right: 10px;"> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
                    <span style="font-size:35px; line-height:70px;">3</span>
                    Adjust the coarse and fine adjustment knobs until the image is focused. 
                    Locate the eyepiece, which extends diagonally toward you. Look through the eyepiece as you adjust the knobs. 
                    Turn the coarse adjustment knob (the larger one) so that the objective lens moves upward and away from the slide in centimeter increments until the image comes into focus. 
                    Now, if necessary, use the fine adjustment knob (the smaller one) to move the lens in millimeter increments for additional clarity.
                </p>
                <p style="text-align: justify;">
                    <ul>  
                        <li style="text-align: justify; margin-left: 10px; margin-right: 10px;">
                        If your microscope has a moving stage, turning the coarse adjustment knob will shift it up and down. 
                        Turn the knob so that the stage moves downward and away from the lens.
                        </li>
                    </ul>
                </p>
                <!-- ending image 8 -->

                <br><br>
          
                <!-- starting image 9 -->
                <img src="images/9.png" class="rounded mx-auto d-block" alt="..." style="width: 300px; height: 300px;">

                <br><br>

                <p style="text-align: justify; margin-left: 10px; margin-right: 10px;"> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
                    <span style="font-size:35px; line-height:70px;">4</span>
                    Switch to the next powerful objective lens and make final focus adjustments. 
                    Rotate the revolving nosepiece to the objective lens that is next in terms of magnification intensity. 
                    After switching upward in intensity, adjust the fine focus knob to make any minor adjustments for clarity. 
                    At this point, your image should only need minimal focusing.
                </p>
                <p style="text-align: justify;">
                    <ul>  
                        <li style="text-align: justify; margin-left: 10px; margin-right: 10px;">
                            If you can't focus the image properly, readjust the focus knob until the objective lens hovers over the image. 
                            Now, repeat the previous steps to readjust the mirror, condenser, and diaphragm, as well as the coarse and fine adjustment knobs.
                        </li>
                    </ul>
                </p>
                <!-- ending image 9 -->

                <br><br>
          
                <!-- starting image 10 -->
                <img src="images/10.png" class="rounded mx-auto d-block" alt="..." style="width: 300px; height: 300px;">

                <br><br>

                <p style="text-align: justify; margin-left: 10px; margin-right: 10px;"> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
                    <span style="font-size:35px; line-height:70px;">5</span>
                    Examine your specimen! Always keep both eyes open. 
                    Even though you're only using one eye to look through the lens, closing the other eye can strain your eyes. 
                    And remember: everything is backward and upside down! Moving the slide to the right puts the image to the left and vice versa.
                </p>
                <p style="text-align: justify;">
                    <ul>  
                        <li style="text-align: justify; margin-left: 10px; margin-right: 10px;">
                            When you're done examining your specimen, turn the objective lens knob until it is at the highest point from the specimen. 
                            Turn the nosepiece back to the lowest power lens, carefully remove the slide, and place a cover on your microscope.
                        </li>
                    </ul>
                </p>
                <!-- ending image 10 -->
            
